package VariableAndDataType;

public class No3 {
    public static void main(String[] args) {

        int a = 6;
        int b = 2;
        int t = a+b;
        int h = a-b;
        int n = a*b;
        int c = a/b;

        // Tại sao thêm String nó lại khôn tính tổng?
        System.out.println(a + " + " + b + " = " + t);
        System.out.println(a + " - " + b + " = " + h);
        System.out.println(a + " * " + b + " = " + n);
        System.out.println(a + " / " + b + " = " + c);



    }
}
